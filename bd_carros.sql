CREATE TABLE carros (
  ID INT NOT NULL AUTO_INCREMENT,
  Marca VARCHAR(50) NOT NULL,
  Modelo VARCHAR(50) NOT NULL,
  Fabricante VARCHAR(50) NOT NULL,
  Tipo VARCHAR(50) NOT NULL,
  Ano INT NOT NULL,
  Combustivel VARCHAR(50) NOT NULL,
  Cor VARCHAR(50) NOT NULL,
  NumChassi VARCHAR(50) NOT NULL,
  Kilometragem INT NOT NULL,
  Revisao BOOLEAN NOT NULL,
  Sinistro BOOLEAN NOT NULL,
  RouboFurto BOOLEAN NOT NULL,
  Aluguel BOOLEAN NOT NULL,
  Venda BOOLEAN NOT NULL,
  Particular BOOLEAN NOT NULL,
  Observacoes TEXT,
  PRIMARY KEY (ID)
);
